	/**
	 * 日期型字符串转化为日期 格式
	 * { "yyyy-MM-dd", "yyyy-MM-dd HH:mm:ss", "yyyy-MM-dd HH:mm", 
	 *   "yyyy/MM/dd", "yyyy/MM/dd HH:mm:ss", "yyyy/MM/dd HH:mm" }
	 */
	public static Date parseDate(Object str) {
		if (str == null){
			return null;
		}
		try {
			return parseDate(str.toString(), parsePatterns);
		} catch (ParseException e) {
			return null;
		}
	}

	/**
	 * 获取过去的天数
	 * @param date
	 * @return
	 */
	public static long pastDays(Date date) {
		long t = new Date().getTime()-date.getTime();
		return t/(24*60*60*1000);
	}
	
    
	public static Date getDateStart(Date date) {
		if(date==null) {
			return null;
		}
		SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
		try {
			date= sdf.parse(formatDate(date, "yyyy-MM-dd")+" 00:00:00");
		} catch (ParseException e) {
			e.printStackTrace();
		}
		return date;
	}
	
	public static Date getDateEnd(Date date) {
		if(date==null) {
			return null;
		}
		SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
		try {
			date= sdf.parse(formatDate(date, "yyyy-MM-dd") +" 23:59:59");
		} catch (ParseException e) {
			e.printStackTrace();
		}
		return date;
	}
	
	/**
	 * 获取服务器日期的前多少天
	 * @param format 日期显示格式
	 * 			yyyy-MM-dd
	 * 			yyyyMMdd
	 * 			.......
	 * @param number	指定前多少天数
	 * @return	java.lang.String
	 */
	public static String getNumberAgoDateStr(String format,int number){
		return getDateFormat(format).format(getDateBefore(new Date(),number));
	}
	
	/**
	 * 获取指定日期的前多少天
	 * @param format 日期显示格式
	 * 			yyyy-MM-dd
	 * 			yyyyMMdd
	 * 			.......
	 * @param number	指定前多少天数
	 * @return	java.lang.String
	 */
	public static String getNumberAgoDateStr(String format,String date,int number){
		Date configDate = null;
		try {
			configDate = getDateFormat(format).parse(date);
		} catch (ParseException e) {
			e.printStackTrace();
		}
		return getDateFormat(format).format(getDateBefore(configDate,number));
	}
	
	/**
	 * 得到几天前的时间 
	 * @param d
	 * @param day
	 * @return
	 */
	public static Date getDateBefore(Date d, int day) {
		Calendar now = Calendar.getInstance();  
		now.setTime(d);  
		now.set(Calendar.DATE, now.get(Calendar.DATE) - day);  
		return now.getTime();  
	}
	
	private static SimpleDateFormat getDateFormat(String format){
		return  new SimpleDateFormat(format);
	}
	
	/**
	 * @declaration: 根据出生日期获取年龄
	 * @param: @param birthday
	 * @param: @return   
	 * @return: int  
	 * @author: wwl E-mail:724045007@qq.com
	 * @date: 2016年10月28日
	 */
	public static int getAgeByBirthday(Date birthday) {
		Calendar cal = Calendar.getInstance();

		if (cal.before(birthday)) {
			throw new IllegalArgumentException("生日必须在当前时间之前!");
		}

		int yearNow = cal.get(Calendar.YEAR);
		int monthNow = cal.get(Calendar.MONTH) + 1;
		int dayOfMonthNow = cal.get(Calendar.DAY_OF_MONTH);

		cal.setTime(birthday);
		int yearBirth = cal.get(Calendar.YEAR);
		int monthBirth = cal.get(Calendar.MONTH) + 1;
		int dayOfMonthBirth = cal.get(Calendar.DAY_OF_MONTH);

		int age = yearNow - yearBirth;

		if (monthNow <= monthBirth) {
			if (monthNow == monthBirth) {
				if (dayOfMonthNow < dayOfMonthBirth) {
					age--;
				}
			} else {
				age--;
			}
		}
		return age;
	}
